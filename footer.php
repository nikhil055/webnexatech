    <script>
        document.addEventListener('DOMContentLoaded', function() {
            const trigger = document.getElementById('chat-trigger');
            const windowBox = document.getElementById('chat-window-box');
            const hideBtn = document.getElementById('hide-chat');
            const chatInput = document.getElementById('user-chat-input');
            const sendBtn = document.getElementById('send-chat-btn');
            const messagesArea = document.getElementById('chat-content-area');

            let currentStep = 0;
            let capturedData = { fullname: '', email: '', phone: '', message: '' };

            trigger.onclick = () => windowBox.classList.toggle('active');
            if(hideBtn) hideBtn.onclick = () => windowBox.classList.remove('active');

            const postMsg = (text, sender) => {
                const div = document.createElement('div');
                div.className = `chat-msg \${sender}-bubble`;
                div.innerText = text;
                messagesArea.appendChild(div);
                messagesArea.scrollTop = messagesArea.scrollHeight;
            };

            const showTyping = () => {
                const typing = document.createElement('div');
                typing.id = 'temp-typing';
                typing.className = 'chat-msg bot-bubble';
                typing.innerHTML = '<span class="typing-dot"></span> <span class="typing-dot"></span> <span class="typing-dot"></span>';
                messagesArea.appendChild(typing);
                messagesArea.scrollTop = messagesArea.scrollHeight;
                return typing;
            };

            const processAI = () => {
                const msg = chatInput.value.trim();
                if(!msg) return;

                postMsg(msg, 'user');
                chatInput.value = '';
                const typing = showTyping();

                setTimeout(() => {
                    typing.remove();
                    if(currentStep === 0) {
                        // User started interaction
                        postMsg("I'd be happy to help! To get started, could you please tell me your Full Name?", 'bot');
                        chatInput.placeholder = "Type your name here...";
                        currentStep = 1;
                    } else if(currentStep === 1) {
                        capturedData.fullname = msg;
                        postMsg(`Nice to meet you, \${msg}! What is your Email Address?`, 'bot');
                        chatInput.placeholder = "Enter your email...";
                        currentStep = 2;
                    } else if(currentStep === 2) {
                        capturedData.email = msg;
                        postMsg("Thanks! And your Phone Number?", 'bot');
                        chatInput.placeholder = "Enter your phone number...";
                        currentStep = 3;
                    } else if(currentStep === 3) {
                        capturedData.phone = msg;
                        postMsg("Got it. Lastly, how can I help you today? Please describe your query or problem.", 'bot');
                        chatInput.placeholder = "Type your problem here...";
                        currentStep = 4;
                    } else if(currentStep === 4) {
                        capturedData.message = msg;
                        postMsg("Perfect! Submitting your details to our experts...", 'bot');
                        
                        fetch('<?php echo BASE_URL; ?>/backend/api/contact.php', {
                            method: 'POST',
                            headers: { 'Content-Type': 'application/json' },
                            body: JSON.stringify(capturedData)
                        })
                        .then(() => {
                            setTimeout(() => {
                                postMsg("Success! 🚀 Your request has been sent. Our team will analyze your problem and contact you shortly!", 'bot');
                                currentStep = 5;
                                chatInput.placeholder = "Inquiry sent!";
                                chatInput.disabled = true;
                                sendBtn.disabled = true;
                            }, 1000);
                        });
                    }
                }, 1200);
            };

            if(sendBtn) sendBtn.onclick = processAI;
            if(chatInput) chatInput.onkeypress = (e) => { if(e.key === 'Enter') processAI(); };
        });
    </script>

